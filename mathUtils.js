
//math functions

export function additionMachine(value1, value2) {
    return value1 + value2; 
}

export function subtractionMachine(value1, value2) {
    return value1 - value2; 
}
export function multiplicationMachine(value1, value2) {
    return value1 * value2; 
}
export function divisionMachine(value1, value2) {
    return value1 / value2; 
}